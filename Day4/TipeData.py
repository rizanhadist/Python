#For Basic Adalah Siktaks untuk melakukan perulangan
#For Looping Array
data = [1,2,3,4]
for a in data:
    print(a)

#For Looping Pada String
text = "BNSP"
for char in text:
    print(char)

#For Looping Pada Set
set_data = {1, 1, 3, 4}
for b in set_data:
    print(b)

#For Looping Pada Tuple (gabisa dimodifikasi)
tuple_data = (2, 3, 4, 5)
for c in tuple_data:
    print(c)

#For Looping Pada Dictionary
dict_data = {
    "name " : "Lorem"
}

for d in dict_data:
    print(d)

#For Range
print(list(range(10)))
print(list(range(10, 20)))
print(list(range(10, 20, 2))) 


#While
i = 0
while i<10:
    print("Itungan", i)
    i = i + 1

